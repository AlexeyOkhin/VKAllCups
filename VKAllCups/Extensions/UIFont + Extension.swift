import UIKit

extension UIFont {
    static func sfPro16Bold() -> UIFont? {
        return UIFont.boldSystemFont(ofSize: 16)
    }

    static func sfPro16Reg() -> UIFont? {
        return UIFont.systemFont(ofSize: 16)
    }
    
    static func sfPro18Bold() -> UIFont? {
        return UIFont.boldSystemFont(ofSize: 18)
    }
}
